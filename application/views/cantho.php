		<p> <h5>TỔNG QUAN DỰ ÁN CẦN THƠ RESIDENCE</h5>
Cần Thơ Residence là dự án đầu tiên và duy nhất triển khai mô hình nhà phố kinh doanh tại Vĩnh Thạnh với quy hoạch hiện đại, kiến trúc và cảnh quang đẹp, đáp ứng hoàn toàn nhu cầu của khách hàng đặt ra cho một dự án bất động sản. Sự kết hợp tuyệt vời giữa chức năng nhà ở với giao dịch thương mại sẽ mang lại cho chủ nhân sở hữu Cần Thơ Residence một không gian sống đáng mơ ước. <br>

– Quy mô dự án: 319.120m² <br>

– Diện tích đất ở : 85.853m² <br>

– Đất chợ, TTTM: 17.931,5m² <br>

– Công trình công cộng: 35.076,5m² <br>

– Bến bãi công cộng: 6.454m² <br>

– Công viên cây xanh: 16.154,58m² <br>

– Nhà máy may VINATEX: 29.723,20m² <br>

– Đất giao thông – Hẻm kỹ thuật: 113.270,97m² <br>

– Mặt nước: 12.044m² <br>

– Vốn đầu tư: 110 tỷ đồng <br>

– Pháp lý dự án: minh bạch, rõ ràng, sở hữu lâu dài, sổ hồng vĩnh viễn. <br>

Dự án Cần Thơ Residence không chỉ là một khu dành riêng cho quý khách hàng tận hưởng cuộc sống với phương châm “SỐNG, TẬN HƯỞNG và KINH DOANH” mà còn là nơi đầu tư lý tưởng, sinh lợi cao. Dự án Cần Thơ Residence sở hữu vẻ đẹp tự nhiên kết hợp phong thủy hữu tình được bao bọc bởi 2 dòng kênh Cái Sắn và kênh Bốn Tổng tạo nên một khu dân cư sang trọng, một môi trường lý tưởng lại đây. Cần Thơ Residence là sự “giao hưởng” hài hòa giữa thiên nhiên và con người sẽ tạo nên những bản sắc văn hóa riêng biệt. <br>

VỊ TRÍ DỰ ÁN CẦN THƠ RESIDENCE <br>
Tọa lạc ngay trung tâm đô thị mới Vĩnh Thạnh sầm uất, liền kề quốc lộ 80 và cao tốc Lộ Tẻ – Rạch Sỏi đặc  biệt là cửa ngõ giao thông phía tây của Tp. Cần Thơ, nơi trực tiếp kết nối giao thông đường bộ với các khu vực lần cận như: Kiên Giang, Long Xuyên, Đồng Tháp… <br>

Vĩnh Thạnh được định hướng quy hoạch phát triển thành vùng kinh tế trọng điểm phía Tây Cần Thơ và trở thành trung tâm đô thị vệ tinh loại IV vào năm 2020. <br>

Hệ thống giao thông hiện đại đã và đang hình thành và đi vào sử dụng có quy mô đồng bộ và hiện đại bậc nhất khu vực phía tây như: cầu Vàm Cống, cao tốc Lộ Tẻ – Rạch Sỏi <br>

Với vị trí đắc địa, liền kề các tiện ích được đầu tư đồng bộ, Cần Thơ Residence là khu dân cư “tiên phong” triển khai theo mô hình nhà phố, vừa mang giá trị kinh doanh vừa đáp ứng đươc nhu cầu “an cư lạc nghiệp” cho khách hàng, đó là điểm nhấn tạo nên sự độc đáo và khác biệt nhất của Cần Thơ Residence trên thị trường bất động sản hiện nay. Cần Thơ Residence tọa lạc ngay trung tâm khu đô thị mới Vĩnh Thạnh, một nơi được mệnh danh là vùng đất hứa ở phía tây Cần Thơ, được bao quanh bởi khu dân cư và các địa điểm thương mại đang phát triển ngày một mạnh mẽ.</p>
